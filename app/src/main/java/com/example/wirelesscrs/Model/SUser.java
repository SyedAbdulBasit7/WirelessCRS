package com.example.wirelesscrs.Model;

public class SUser{
    public String name;
    public String email;
    public int age;
    public String gender;
    public String department;

    public SUser(){}

    public SUser(String name, String email, int age,String gender, String department){
        this.name=name;
        this.email=email;
        this.age=age;
        this.gender=gender;
        this.department=department;

    }
}